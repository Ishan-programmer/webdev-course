const month = 3
switch (month) {
    case 1:
        console.log("Januaray")
        break;
    case 2:
        console.log("Februaray")
        break;
    case 3:
        console.log("march")
        break;
    case 4:
        console.log("April")
        break;
    case 5:
        console.log("May")
        break;
    
    default:
        console.log("default case march")
        break;
}

//if break statement is not used, then all the cases after switch statement becomes true are executed EXCEPT Default